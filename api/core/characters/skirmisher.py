    def 伤害计算(self):
        data = super().伤害计算()
        # 拆分统计
        if self.get_individuation(0) == 1:
            被动伤害 = 0
            行云 = self.get_skill_by_name('行云：冥')
            for skill in 行云.行云技能:
                detail = data['skills'].get(skill,None)
                if detail is not None:
                    行云倍率 = self.get_skill_by_name(skill).行云倍率
                    detail['damage'] /= 行云倍率
                    detail['atk_rate'] /= 行云倍率
                    被动伤害 += detail['damage'] * (行云倍率-1)
            if 被动伤害 > 0:
                data['skills']['行云：冥'] = {
                'name': '行云：冥',
                'rate': 1.0,
                'cd': 1.0,
                'mp': 0,
                'damage': 被动伤害,
                'count': 1,
                'level': 行云.等级,
                'cosume_cube_frag': 0,
                'cd_o': 1.0,
                'atk_rate': 1.0
            }
        return data