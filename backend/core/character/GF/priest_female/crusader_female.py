    def load_skills(self):
        """加载技能"""
        skills = []
        skills_dict = {}
        i = 0
        while i >= 0:
            try:
                tem = eval('Skill' + str(i) + '(char=self)')
                skills_dict[tem.name] = tem
                skills.append(tem)
                if tem.buffer and tem.buffType == 'buff' and tem.learnLv == 30:
                    self.BuffSkill = tem
                if tem.buffer and tem.buffType == 'awake':
                    self.AwakeSkill = tem
                i += 1
            except Exception:
                i = -1
        self.skills = skills
        self.skills_dict = skills_dict
